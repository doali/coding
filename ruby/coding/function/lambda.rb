#!/usr/bin/env ruby

# ------------------------------------------------------------------------------
# http://rubyforadmins.com/blocks
# lambda functions
# ------------------------------------------------------------------------------

# ------------------------------------------------------------------------------
# Syntax 1 : `->`
l_1 = ->(x) { puts "#{x}" }

l_1.call(1) # lambda functions are objects (Proc object instance)

# ------------------------------------------------------------------------------
# Syntax 2 : block
l_2 = lambda { |x| puts "#{x}" } # using block syntax

l_2.call(2)

# ------------------------------------------------------------------------------
# Syntax 3 : block
l_3 = lambda do |x, y|
  printf "Sum #{x} + #{y} = "
  puts "#{x + y}"
end

l_3.call(1, 2)
